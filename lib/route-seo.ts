function hashRoute(input: string) {
  let h = 0;
  for (let i = 0; i < input.length; i += 1) {
    h = (h * 31 + input.charCodeAt(i)) >>> 0;
  }
  return h;
}

function pick<T>(items: T[], hash: number, shift = 0) {
  return items[(hash + shift) % items.length];
}

function pickMany<T>(items: T[], hash: number, count: number, step = 3) {
  const out: T[] = [];
  const used = new Set<number>();

  for (let i = 0; out.length < Math.min(count, items.length); i += 1) {
    const index = (hash + i * step) % items.length;
    if (used.has(index)) continue;
    used.add(index);
    out.push(items[index]);
  }

  return out;
}

